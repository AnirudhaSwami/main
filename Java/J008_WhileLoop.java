/*

Challenge: Count from 1 to 5
Test your understanding of Java While Loops by completing a small coding challenge.

Instructions
Inside main(), complete the following steps:
Declare an int variable named i and set it to 1
Use a while loop that runs as long as i <= 5
Inside the loop, print the value of i
Increase i by 1 in each loop

*/

public class J008_WhileLoop {
    public static void main(String [] args){
       int i = 1;
       while(i <= 5){
        System.out.println(i);
        i++;
       } 
    }    
}
